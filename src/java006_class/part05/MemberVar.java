package java006_class.part05;

public class MemberVar {
	// 멤버변수는 기본값을 제공한다. (JVM)
	byte var_byte; // 0
	short var_short; // 0
	int var_int; // 0
	long var_long; // 0L
	float var_float; // 0.0F 실수는 기본적으로 double로 인식한다.
	double var_double; // 0.0 
	boolean var_boolean; // false
	char var_char; // \u0000 유니코드
	String var_string; // null
	char[] var_arr; // null
	// 참조데이터타입은 모두 기본값으로 null값을 가진다.
	
}
